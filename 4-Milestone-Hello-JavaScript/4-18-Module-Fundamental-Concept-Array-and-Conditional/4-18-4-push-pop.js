//Declare variable
var friendName = ['Ziad', 'Fuad', 'Nishat', 'Nahid'];
var friendAge = [25, 32, 28, 27];
var friendIncome = [500, 2000, 0, 1000];

//Push string to the last of an array
friendName.push('Tashin');
console.log(friendName);

//Push number to the last of an array
friendAge.push(10);
console.log(friendAge);


//Pop string from the last of an array
friendName.pop();
console.log(friendName);

//Pop number from the last of an array
friendAge.pop();
console.log(friendAge);

//Unshift string to the first of an array
friendName.unshift('Tashin');
console.log(friendName);

//Unshift number to the first of an array
friendAge.unshift(10);
console.log(friendAge);

//Shift string from the last of an array
friendName.shift();
console.log(friendName);

//Shift number from the last of an array
friendAge.shift();
console.log(friendAge);

